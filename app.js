var btns = document.querySelectorAll('.btn');

var current = document.querySelector('.active');
document.getElementById(current.name).style = "display: initial";

const tbody = document.getElementById('tbody');

data.forEach(team => {
    j = 1
    l = team.length;
    s = '<th>' + team[0] + '</th>'
    for (i = 1; i < 5; i++) {
        if (j < l) {
            s += '<td>' + team[j] + '</td>'
        } else {
            s += '<td>-</td>';
        }
        j += 1;
    }
    tbody.innerHTML += '<tr class="want-hover">' + s + '</tr>';
})


btns.forEach(btn => {
    btn.addEventListener('click', function () {
        var current = document.querySelector('.active');
        current.className = current.className.replace(' active');
        this.className += ' active';
        document.getElementById(current.name).style = "display: none";
        document.getElementById(this.name).style = "display: initial";
    })
});